function soma (x, y){
    let total = x + y;
    return total;
};

function subitracao(x, y){
    let total = x - y;
    return total;
};

function multiplicacao(x, y){
    let total = x * y;
    return total;
};

function divisao(x, y){
    let total = x / y;
    return total;
};

export {soma, subitracao, multiplicacao, divisao};